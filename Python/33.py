# 33.Crie um algoritmo que receba a idade de três pessoas, calcule e mostre a média das idades.
idade1 = int(input("Informe a idade 1: "));
idade2 = int(input("Informe a idade 2: "));
idade3 = int(input("Informe a idade 3: "));
media = (idade1+idade2+idade3)/3;
print("A média das idades é de ",media);